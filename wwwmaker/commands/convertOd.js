let filePaths = [];
function listFile(mdDir) {
  // .mdディレクトリを再帰的に検索する
  let dirs = fs.readdirSync(mdDir);
  dirs.forEach((d) => {
    let mdPath = mdDir + d;
    let stats = fs.statSync(mdPath);
    if (stats.isDirectory()) {
      listFile(mdPath + '/');
    } else if (stats.isFile() && d.match(/\.md$/)) {
      filePaths.push(mdPath);
    }
  });
}

listFile(blogConfig.mdDir);
let convertFiles = [];
for(const filePath of filePaths){
  let doc = fs.readFileSync(filePath,'utf-8');
  const re = /(?:https?:)?\/\/.*?livefilestore\.com\/[^/]*?\/(.*?)\?psid=1/ig;
  if(doc.match(re)){
    doc = doc.replace(/(?:https?:)?\/\/.*?livefilestore\.com\/[^/]*?\/(.*?)\?psid=1/ig,function(){
      for(const img of imagePaths){
        if(img.match(arguments[1])) return img;
      }
      return arguments[0];
    });
    fs.writeFileSync(filePath,doc,'utf-8');
    convertFiles.push(filePath);
  }
}

